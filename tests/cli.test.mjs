test('init supports WordPress themes with enqueue-ready CSS output', () => {
  const cwd = tempProject()
  mkdirSync(join(cwd, 'templates'), { recursive: true })
  mkdirSync(join(cwd, 'parts'), { recursive: true })
  writeFileSync(join(cwd, 'functions.php'), '<?php\n')

  run(['init', '--cwd', cwd, '--preset', 'wordpress'])

  const config = readFileSync(join(cwd, 'synced-fluid.config.mjs'), 'utf8')
  const cssOutput = readFileSync(join(cwd, 'assets/css/synced-fluid.css'), 'utf8')

  assert.match(config, /scan: \['templates', 'parts', 'src'\]/)
  assert.match(config, /out: 'assets\/css\/synced-fluid.css'/)
  assert.match(config, /includeCore: true/)
  assert.match(cssOutput, /Generated by @synced\/fluid/)
  assert.equal(existsSync(join(cwd, 'src/synced-fluid.css')), false)

  run(['build', '--cwd', cwd])
  const builtCssOutput = readFileSync(join(cwd, 'assets/css/synced-fluid.css'), 'utf8')
  assert.match(builtCssOutput, /\.sf-skip-link/)
  assert.match(builtCssOutput, /:where\(\.sf-visually-hidden, \.sr-only\)/)

  const doctor = run(['doctor', '--cwd', cwd])
  assert.match(doctor, /pass Core CSS is included in generated output/)
})

