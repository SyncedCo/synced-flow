test('package exposes modular CSS layer files', () => {
  const pkg = JSON.parse(readFileSync(join(packageRoot, 'package.json'), 'utf8'))
  const files = ['tokens.css', 'reset.css', 'base.css', 'app.css', 'layout.css', 'components.css', 'utilities.css', 'styles.css']

  for (const file of files) {
    assert.equal(pkg.exports[`./${file}`], `./${file}`)
    assert.match(readFileSync(join(packageRoot, file), 'utf8'), /@layer reset, tokens, base, app, layout, components, utilities;/)
  }

  const baseCss = readFileSync(join(packageRoot, 'base.css'), 'utf8')
  const appCss = readFileSync(join(packageRoot, 'app.css'), 'utf8')
  const utilitiesCss = readFileSync(join(packageRoot, 'utilities.css'), 'utf8')
  assert.match(baseCss, /text-decoration-color: color-mix\(in oklch, currentColor 55%, transparent\)/)
  assert.doesNotMatch(baseCss, /:where\(a\) \{ color: inherit; text-decoration: none;/)
  assert.match(appCss, /@layer app/)
  assert.match(appCss, /text-decoration: none/)
  assert.match(appCss, /list-style: none/)
  assert.match(utilitiesCss, /:where\(\.sf-visually-hidden, \.sr-only\)/)
  assert.match(utilitiesCss, /\.sf-skip-link/)
  assert.match(utilitiesCss, /\.sf-list-reset/)
  assert.match(utilitiesCss, /\.sf-link-plain/)
})

